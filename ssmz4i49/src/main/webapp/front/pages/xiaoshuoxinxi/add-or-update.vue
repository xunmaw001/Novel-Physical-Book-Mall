<script>
	import wPicker from "@/components/w-picker/w-picker.vue";

	export default {
		data() {
			return {
				ruleForm: {
												xiaoshuomingcheng: '',
																fenlei: '',
																tupian: '',
																zuozhe: '',
																chubanshe: '',
																zongzhangjie: '',
																xiaoshuojianjie: '',
																clicktime: '',
																clicknum: '',
																price: '',
												},
																				fenleiOptions: [],
				fenleiIndex: 0,
																																																																												// 登陆用户信息
				user: {},
                                ro:{
                                   xiaoshuomingcheng : false,
                                   fenlei : false,
                                   tupian : false,
                                   zuozhe : false,
                                   chubanshe : false,
                                   zongzhangjie : false,
                                   xiaoshuojianjie : false,
                                   clicktime : false,
                                   clicknum : false,
                                   price : false,
                                },
			}
		},
		components: {
			wPicker
		},
		computed: {
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
					},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
						// ss读取
																																																																		
																		// 下拉框
			res = await this.$api.option(`xiaoshuofenlei`,`fenlei`,{});
			this.fenleiOptions = res.data;
																																																									
			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`xiaoshuoxinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='xiaoshuomingcheng'){
					this.ruleForm.xiaoshuomingcheng = obj[o];
					this.ro.xiaoshuomingcheng = true;
					continue;
					}
					if(o=='fenlei'){
					this.ruleForm.fenlei = obj[o];
					this.ro.fenlei = true;
					continue;
					}
					if(o=='tupian'){
					this.ruleForm.tupian = obj[o];
					this.ro.tupian = true;
					continue;
					}
					if(o=='zuozhe'){
					this.ruleForm.zuozhe = obj[o];
					this.ro.zuozhe = true;
					continue;
					}
					if(o=='chubanshe'){
					this.ruleForm.chubanshe = obj[o];
					this.ro.chubanshe = true;
					continue;
					}
					if(o=='zongzhangjie'){
					this.ruleForm.zongzhangjie = obj[o];
					this.ro.zongzhangjie = true;
					continue;
					}
					if(o=='xiaoshuojianjie'){
					this.ruleForm.xiaoshuojianjie = obj[o];
					this.ro.xiaoshuojianjie = true;
					continue;
					}
					if(o=='clicktime'){
					this.ruleForm.clicktime = obj[o];
					this.ro.clicktime = true;
					continue;
					}
					if(o=='clicknum'){
					this.ruleForm.clicknum = obj[o];
					this.ro.clicknum = true;
					continue;
					}
					if(o=='price'){
					this.ruleForm.price = obj[o];
					this.ro.price = true;
					continue;
					}
				}
			}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
																																																																		
			// 多级联动参数
																																																																		
																																																															
																																																			// 日长控件选择日期时间
			clicktimeConfirm(val) {
				console.log(val)
				this.ruleForm.clicktime = val.result;
				this.$forceUpdate();
			},
																		
															// 下拉变化
			fenleiChange(e) {
				this.fenleiIndex = e.target.value
				this.ruleForm.fenlei = this.fenleiOptions[this.fenleiIndex]
			},
																																																						
																											tupianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.tupian = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},
																																																																								
			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
																																																																																																																																																																																																																																																																																																																																																																																																				if((!this.ruleForm.xiaoshuomingcheng)){
					this.$utils.msg(`小说名称不能为空`);
					return
				}
																																												if((!this.ruleForm.fenlei)){
					this.$utils.msg(`分类不能为空`);
					return
				}
																																												if((!this.ruleForm.tupian)){
					this.$utils.msg(`图片不能为空`);
					return
				}
																																																																																																																																																																																																																																				if(this.ruleForm.clicknum&&(!this.$validate.isIntNumer(this.ruleForm.clicknum))){
					this.$utils.msg(`点击次数应输入整数`);
					return
				}
																																								if((!this.ruleForm.price)){
					this.$utils.msg(`价格不能为空`);
					return
				}
																if(this.ruleForm.price&&(!this.$validate.isNumber(this.ruleForm.price))){
					this.$utils.msg(`价格应输入数字`);
					return
				}
																																if(this.ruleForm.id){
					await this.$api.update(`xiaoshuoxinxi`, this.ruleForm);
				}else{
					await this.$api.add(`xiaoshuoxinxi`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>

